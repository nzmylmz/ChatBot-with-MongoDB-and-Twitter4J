    private void btnSearchModelOnTwitterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchModelOnTwitterActionPerformed
        double avarage = 0;
        String hastag = "#" + jListModel.getSelectedValue();
        hastag = hastag.replaceAll("\\s", "");
        AnalyseTweets analyse = new AnalyseTweets(hastag);
        ArrayList<TWEETS> tweet = analyse.analyse();

        avarage = dosyayaYaz(avarage, tweet);
        jLabel11.setText("<html>Tweeter üzerindeki<br/>yorumlara göre<br/>ortalama polarite:<br/>"+ avarage+"</html>" );


    }//GEN-LAST:event_btnSearchModelOnTwitterActionPerformed
    private double dosyayaYaz(double avarage, ArrayList<TWEETS> tweet) {//Product sekmesinde yapılan arama sonucunu polariteYorumlar.txt dosyasına yazar
        //ortalama polarite değerini geri döndürür-product tabındaki label'a yazmak için

        try {
            File dosya = new File("polariteYorumlar.txt");
            FileWriter yazici = new FileWriter(dosya, true);
            BufferedWriter yaz = new BufferedWriter(yazici);

            for (int i = 0; i < 10; i++) {

                yaz.write("[ " + (i + 1) + " ]" + tweet.get(i).tweet + "--------->" + tweet.get(i).polarity + "\n");
                yaz.newLine();
                avarage += tweet.get(i).polarity;
            }
            yaz.close();
        } catch (IOException hata) {
            JOptionPane.showMessageDialog(null, hata.toString(), "WARNING", JOptionPane.WARNING_MESSAGE);
        }

        return avarage;
    }

    private void listRead(ArrayList<String> donut) {//donut Arraylist'i TextArea'ya eklenir.
        for (String a : donut) {
            jTextArea1.append(a + "\n");
        }
    }
    DBCollection dbcol;

    public void mongoConnection(String host, Integer portNo, String dbName, String collectionName) {

        try {
            MongoClient mongoClient = new MongoClient(host, portNo);
            //mongodb'ye bağlanmak için connection hattı oluştur.

            DB db = mongoClient.getDB(dbName);//bağlanılan veritabanı

            dbcol = db.getCollection(collectionName);//bağlanılan collection

        } catch (UnknownHostException ex) {
            JOptionPane.showMessageDialog(null, ex.toString(), "ERROR0", JOptionPane.ERROR_MESSAGE);//hata msj'ı için messagebox
        }
    }

    public void getCategory() {
        Category cat = new Category();
        uploadToLists(jListCategory, cat.getCategoryRecord(dbcol));
    }

    public void uploadToLists(JList list, Iterable<DBObject> output) {//gruplanan alanları ilgili list'lere yükle

        DefaultListModel dlm = new DefaultListModel();

        for (DBObject dbObject : output) {
            String[] element = dbObject.toString().split("\"");
            dlm.addElement(element[3]);
        }
        list.setModel(dlm);
    }

