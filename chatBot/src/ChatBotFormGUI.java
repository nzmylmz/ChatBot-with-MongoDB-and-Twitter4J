    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int dialogResult = JOptionPane.showConfirmDialog(null, "Seçili kaydı silmek istediğinizden emin misiniz?",
                "WARNING", JOptionPane.YES_NO_OPTION);

        if (dialogResult == JOptionPane.YES_OPTION) {
            BasicDBObject document = new BasicDBObject();
            if (!jListCategory.isSelectionEmpty()) {
                document.put("category", jListCategory.getSelectedValue());
            }
            if (!jListType.isSelectionEmpty()) {
                document.put("type", jListType.getSelectedValue());
            }
            if (!jListBrand.isSelectionEmpty()) {
                document.put("brand", jListBrand.getSelectedValue());
            }
            if (!jListModel.isSelectionEmpty()) {
                document.put("model", jListModel.getSelectedValue());
            }
            dbcol.remove(document);

            JOptionPane.showMessageDialog(null, "Seçili kayıt silindi", "INFORMATION", JOptionPane.INFORMATION_MESSAGE);

            getCategory();

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

